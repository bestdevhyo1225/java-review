package com.hyoseok.review.special.example1;

public class SpecialCharacterMain {
    public static void main(String[] args) {
        // 특수문자
        System.out.println("Good\tMorning~!");  // \t 탭
        System.out.println("Good\nMorning~!");  // \n 줄바꿈
        System.out.println("Good\'Morning~!\'");  // \' '
        System.out.println("Good\"Morning~!\"");  // \" "
        System.out.println("Good\\Morning~!\\");  // \\ 역 슬래시
    }
}
